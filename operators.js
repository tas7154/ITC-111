// Operators - allow you to perform different operations on variable values

// Arithmetic Operators - used to perform on numbers 
/*
    + : for addition
    - : for subtraction
    * : for multiplication
    / : for division
    % : for modulus
    ++ : to increment variable value by 1
    -- : to decrement variable value by 1
*/

// Assignment Operators - use to assign value to variable
var x = 5; var y = 5;
/*
    = : assign value to variable
    += : same as x = x + y-> value of x is 10
    -= : same as x = x - y-> value of x is 0
    *= : same as x = x * y-> value of x is 25
    /= : same as x = x / y-> value of x is 1
    %= : same as x = x % y-> value of x is 0
    **= : same as x = x + y-> value of x is 3125
*/

// String Operators - use to concatenate strings
/*
    + : adds one string to another
    += : adds new string value to an exiting string
*/

// Comparison Operators - used to check difference between variables
/*
    == : use to check if one valus is equal to another
    === : use to check if  value and type is equal
    != : use to check if not equal
    !== : used to check if value and type are not equal
    > : used to check if value is greater than
    < : used to check if value is less than
    >= : used to check if value is greater than or equal to
    <= : used to check if value is less than or equal to
    ? : used to asign value of a condition to a variable if true
*/

// Logical Operators - used to test logical
/*
    && : logical and
    || : logical or
    ! : logical not
*/


